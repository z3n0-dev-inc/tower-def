function drawMapPreview(canvas, map) {
  const ctx=canvas.getContext('2d');
  const W=canvas.width, H=canvas.height;
  const tW=W/map.cols, tH=H/map.rows;
  const seed=map.id.split('').reduce((a,c)=>a+c.charCodeAt(0),0);
  const rng=mulberry32Preview(seed*7+3);
  const rng2=mulberry32Preview(seed*13+9);

  const TC={
    graveyard:{bg1:'#141f10',bg2:'#1c2b15',acc:'#253d1c'},
    urban:    {bg1:'#111214',bg2:'#181b1e',acc:'#242628'},
    volcanic: {bg1:'#120400',bg2:'#1c0700',acc:'#280c00'},
    arctic:   {bg1:'#08111e',bg2:'#0c1928',acc:'#142035'},
    hell:     {bg1:'#0c0000',bg2:'#130000',acc:'#1c0000'},
    nuclear:  {bg1:'#091000',bg2:'#0f1800',acc:'#172300'},
    shadow:   {bg1:'#020206',bg2:'#04040c',acc:'#080818'},
    omega:    {bg1:'#000002',bg2:'#010006',acc:'#04000c'},
  };
  const th=TC[map.theme]||TC.graveyard;

  const bg=ctx.createLinearGradient(0,0,W,H);
  bg.addColorStop(0,th.bg1); bg.addColorStop(0.6,th.bg2); bg.addColorStop(1,th.bg1);
  ctx.fillStyle=bg; ctx.fillRect(0,0,W,H);

  for(let i=0;i<180;i++){ctx.globalAlpha=0.08+rng2()*0.12;ctx.fillStyle=th.acc;ctx.fillRect(rng2()*W,rng2()*H,rng2()<0.3?2:1,rng2()<0.3?2:1);}
  ctx.globalAlpha=1;

  const PC={
    graveyard:{p1:'#4f3e22',p2:'#7a6040'},
    urban:    {p1:'#484848',p2:'#686868'},
    volcanic: {p1:'#3d1500',p2:'#6a2800'},
    arctic:   {p1:'#7aaac8',p2:'#b0d0e8'},
    hell:     {p1:'#580000',p2:'#880000'},
    nuclear:  {p1:'#445e00',p2:'#607a00'},
    shadow:   {p1:'#220055',p2:'#440099'},
    omega:    {p1:'#380000',p2:'#660000'},
  };
  const pc=PC[map.theme]||PC.graveyard;
  const T=Math.min(tW,tH);

  const strokeP=(lw,col,alpha)=>{
    ctx.save(); if(alpha!==undefined)ctx.globalAlpha=alpha;
    ctx.strokeStyle=col; ctx.lineWidth=T*lw;
    ctx.lineCap='round'; ctx.lineJoin='round';
    ctx.beginPath();
    map.path.forEach(([c,r],i)=>{
      const px=c*tW+tW/2, py=r*tH+tH/2;
      i===0?ctx.moveTo(px,py):ctx.lineTo(px,py);
    });
    ctx.stroke(); ctx.restore();
  };
  strokeP(1.1,'rgba(0,0,0,0.7)');
  strokeP(0.9,pc.p1);
  strokeP(0.45,pc.p2,0.55);

  const step=Math.max(4,Math.floor(map.path.length/10));
  ctx.fillStyle=pc.p2; ctx.globalAlpha=0.6;
  for(let i=step;i<map.path.length-1;i+=step){
    const [c,r]=map.path[i],[nc,nr]=map.path[i+1];
    const px=c*tW+tW/2, py=r*tH+tH/2;
    const angle=Math.atan2(nr-r,nc-c);
    const sz=Math.min(tW,tH)*0.3;
    ctx.save(); ctx.translate(px,py); ctx.rotate(angle);
    ctx.beginPath(); ctx.moveTo(sz,0); ctx.lineTo(-sz*0.55,-sz*0.5); ctx.lineTo(-sz*0.55,sz*0.5); ctx.closePath(); ctx.fill();
    ctx.restore();
  }
  ctx.globalAlpha=1;

  const [sc,sr]=map.path[0],[ec,er]=map.path[map.path.length-1];
  _drawPortalPrev(ctx,sc*tW+tW/2,sr*tH+tH/2,Math.min(tW,tH)*0.5,'#27ae60','#2ecc71','S');
  _drawPortalPrev(ctx,ec*tW+tW/2,er*tH+tH/2,Math.min(tW,tH)*0.5,'#c0392b','#e74c3c','E');

  const vig=ctx.createRadialGradient(W/2,H/2,H*0.1,W/2,H/2,H*0.9);
  vig.addColorStop(0,'rgba(0,0,0,0)'); vig.addColorStop(1,'rgba(0,0,0,0.6)');
  ctx.fillStyle=vig; ctx.fillRect(0,0,W,H);
}

function _drawPortalPrev(ctx,x,y,r,c1,c2,label){
  const g=ctx.createRadialGradient(x,y,0,x,y,r*2.2);
  g.addColorStop(0,c2+'66'); g.addColorStop(1,c1+'00');
  ctx.fillStyle=g; ctx.beginPath(); ctx.arc(x,y,r*2.2,0,Math.PI*2); ctx.fill();
  ctx.strokeStyle=c2; ctx.lineWidth=r*0.22;
  ctx.beginPath(); ctx.arc(x,y,r,0,Math.PI*2); ctx.stroke();
  ctx.fillStyle=c1+'99'; ctx.beginPath(); ctx.arc(x,y,r*0.68,0,Math.PI*2); ctx.fill();
  ctx.fillStyle='#fff';
  ctx.font=`bold ${Math.floor(r*1.0)}px monospace`;
  ctx.textAlign='center'; ctx.textBaseline='middle';
  ctx.shadowBlur=5; ctx.shadowColor='#000'; ctx.fillText(label,x,y); ctx.shadowBlur=0;
}

function mulberry32Preview(seed){
  return function(){
    let t=seed+=0x6D2B79F5;
    t=Math.imul(t^t>>>15,t|1); t^=t+Math.imul(t^t>>>7,t|61);
    return((t^t>>>14)>>>0)/0xFFFFFFFF;
  };
}
